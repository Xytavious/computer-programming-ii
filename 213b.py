q1 = int(input("Enter the Quantity: "))


price = 0
am = price * q1
if 0 < q1 <= 99:
  price = 5.95
  am = price * q1
  print("Amount Due: ", am)

elif 100 <= q1 <= 199:
  price = 5.75
  am = price * q1
  print("Amount Due: ", am)

elif 200 <= q1 <= 299:
  price = 5.40
  am = price * q1
  print("Amount Due: ", am)

elif 300 <= q1 > 10000000:
  price = 5.15
  am = price * q1
  print("Amount Due: ", am)


print(price)

